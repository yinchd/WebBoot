package com.yinchd.web.constants;

/**
 * 正则表达式集合
 * @author yinchd
 * @since 2019/3/18-11:55
 **/
public interface RegexConstants {

    /**
     * 账号
     */
    String ACCOUNT_EN = "^[A-Za-z_@.0-9]{4,20}$";
    /**
     * 姓名
     */
    String USERNAME = "^[\\u4E00-\\u9FA5\\uf900-\\ufa2d·s]{2,20}$";
    /**
     * 用户名，2~20位
     */
    String ACCOUNT = "^[a-zA-Z\\u4E00-\\u9FA5\\uf900-\\ufa2d@0-9_-·*s]{2,20}$";
    /**
     * 密码：8-16位包含大小写字母、数字
     */
    String PASSWORD_8_16 = "^(?=.*\\d)(?=.*[a-z])(?=.*[A-Z]).{8,16}$";
    /**
     * 邮箱
     */
    String EMAIL = "[\\w!#$%&'*+/=?^_`{|}~-]+(?:\\.[\\w!#$%&'*+/=?^_`{|}~-]+)*@(?:[\\w](?:[\\w-]*[\\w])?\\.)+[\\w](?:[\\w-]*[\\w])?";
    /**
     * 座机
     */
    String TELEPHONE = "0\\d{2,3}-\\d{7,8}";
    /**
     * 手机
     */
    String PHONE = "^[1]([3-9])[0-9]{9}$";
    /**
     * 18位身份证
     */
    String IDCARD = "^[1-9]\\d{5}[1-9]\\d{3}((0\\d)|(1[0-2]))(([0|1|2]\\d)|3[0-1])\\d{3}([0-9]|X)$";
    /**
     * IPV4校验
     */
    String IPV4 = "\\b(?:(?:25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)\\.){3}(?:25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)\\b";
    /**
     * IPV6校验
     */
    String IPV6 = "(([0-9a-fA-F]{1,4}:){7,7}[0-9a-fA-F]{1,4}|([0-9a-fA-F]{1,4}:){1,7}:|([0-9a-fA-F]{1,4}:){1,6}:[0-9a-fA-F]{1,4}|([0-9a-fA-F]{1,4}:){1,5}(:[0-9a-fA-F]{1,4}){1,2}|([0-9a-fA-F]{1,4}:){1,4}(:[0-9a-fA-F]{1,4}){1,3}|([0-9a-fA-F]{1,4}:){1,3}(:[0-9a-fA-F]{1,4}){1,4}|([0-9a-fA-F]{1,4}:){1,2}(:[0-9a-fA-F]{1,4}){1,5}|[0-9a-fA-F]{1,4}:((:[0-9a-fA-F]{1,4}){1,6})|:((:[0-9a-fA-F]{1,4}){1,7}|:)|fe80:(:[0-9a-fA-F]{0,4}){0,4}%[0-9a-zA-Z]{1,}|::(ffff(:0{1,4}){0,1}:){0,1}((25[0-5]|(2[0-4]|1{0,1}[0-9]){0,1}[0-9])\\.){3,3}(25[0-5]|(2[0-4]|1{0,1}[0-9]){0,1}[0-9])|([0-9a-fA-F]{1,4}:){1,4}:((25[0-5]|(2[0-4]|1{0,1}[0-9]){0,1}[0-9])\\.){3,3}(25[0-5]|(2[0-4]|1{0,1}[0-9]){0,1}[0-9]))";
    /**
     * url地址
     */
    String URL = "^(http|https|ftp):\\/\\/([a-zA-Z0-9\\.-]+(:[a-zA-Z0-9\\.&%$-]+)*@)*"
            + "((25[0-5]|2[0-4][0-9]|[0-1]{1}[0-9]{2}|[1-9]{1}[0-9]{1}|[1-9])\\.(25[0-5]|2[0-4][0-9]|[0-1]{1}[0-9]{2}|[1-9]{1}[0-9]{1}|[1-9]|0)"
            + "\\.(25[0-5]|2[0-4][0-9]|[0-1]{1}[0-9]{2}|[1-9]{1}[0-9]{1}|[1-9]|0)\\.(25[0-5]|2[0-4][0-9]|[0-1]{1}[0-9]{2}|[1-9]{1}[0-9]{1}|[0-9])"
            + "|localhost|([a-zA-Z0-9-]+\\.)*[a-zA-Z0-9-]+\\.(com|edu|gov|int|mil|net|org|biz|arpa|info|name|pro|aero|coop|museum|[a-zA-Z]{2}))(:[0-9]+)*(\\/($|[a-zA-Z0-9\\.,?\\'\\\\+&%$#=~_-]+))*$";
    /**
     * 统一社会信用代码
     */
    String UNIFIED_SOCIAL_CREDIT_CODE = "^([159Y]{1})([1239]{1})([0-9ABCDEFGHJKLMNPQRTUWXY]{6})([0-9ABCDEFGHJKLMNPQRTUWXY]{9})([0-90-9ABCDEFGHJKLMNPQRTUWXY])$";

    /**
     * 汉字
     */
    String CHINESE_CHARACTER = "^[\\u4e00-\\u9fa5]{0,}$";

    /**
     * 通用的一些名称校验正则
     */
    String COMMON_NAME = "^[\\u4e00-\\u9fa5a-zA-Z-_]{2,20}$";

    /**
     * 角色名称
     */
    String ROLE_NAME = "^[\\u4e00-\\u9fa50-9.a-zA-Z]{2,50}$";
    /**
     * token过期时间校验
     */
    String TOKEN_EXPIRE_FORMAT = "^\\d+[Mdhms]$";
    /**
     * 英文字母校验
     */
    String EN_CHARACTER = "[A-Za-z]";

}
